// ✅ Formateador de precios en MXN
const formatter = new Intl.NumberFormat("es-MX", {
    style: "currency",
    currency: "MXN",
});

// Inicialización
document.addEventListener("DOMContentLoaded", () => {
    const loader = document.getElementById("loader");
    if (loader) {
        setTimeout(() => {
            loader.classList.add("loader-hidden");
            loader.addEventListener("transitionend", () => {
                loader.remove();
            });
        }, 1500);
    }

    initMobileMenu();

    const path = window.location.pathname;

    if (path.includes("index.html") || path === "/") {
        renderProducts("Todos");
        initFilters();
    } else if (path.includes("producto.html")) {
        loadProductDetail();
    }
});

// Menú Móvil
function initMobileMenu() {
    const menuBtn = document.querySelector(".mobile-menu-btn");
    const nav = document.querySelector(".mobile-nav");
    const closeBtn = document.querySelector(".close-menu");

    if (menuBtn) {
        menuBtn.addEventListener("click", () => {
            nav.classList.add("active");
        });

        if (closeBtn) {
            closeBtn.addEventListener("click", () => {
                nav.classList.remove("active");
            });
        }
    }
}

// Renderizar Productos en Grid
function renderProducts(category) {
    const grid = document.getElementById("products-grid");
    if (!grid) return;

    grid.innerHTML = "";

    const filtered =
        category === "Todos" ?
        products :
        products.filter((p) => p.category === category);

    filtered.forEach((product) => {
                const card = document.createElement("div");
                card.className = "product-card";

                let badgesHtml = "";
                if (product.isNew) {
                    badgesHtml += `<span class="badge badge-new">Nuevo</span>`;
                }

                if (product.inStock) {
                    if (!product.isNew) {
                        badgesHtml += `<span class="badge badge-available">Disponible</span>`;
                    }
                } else {
                    badgesHtml += `<span class="badge badge-out">Agotado</span>`;
                }

                const imageClass = product.inStock ? "" : "grayscale";

                card.innerHTML = `
      <div style="position: relative; overflow: hidden;">
        <a href="producto.html?id=${product.id}">
          ${badgesHtml}
          <img
            src="${product.image}"
            alt="${product.name}"
            class="card-image ${imageClass}"
            onerror="this.src='IMG/placeholder.svg?height=400&width=400'"
          >
        </a>
        ${
  product.inStock ?
   ` <div class="card-actions"> <a href="producto.html?id=${product.id}" class="btn-card-buy"> Comprar ahora </a> </div> ` : "" } </div> <div class="card-info"> <a href="producto.html?id=
   ${product.id}" style="text-decoration: none; color: inherit;"> <h3 class="product-name"
   >${product.name}</h3> <p class="product-price">${formatter.format(product.price)}</p> </a> </div> `; grid.appendChild(card);
  });
}

// Filtros
function initFilters() {
  const buttons = document.querySelectorAll(".filter-btn");
  buttons.forEach((btn) => {
    btn.addEventListener("click", (e) => {
      buttons.forEach((b) => b.classList.remove("active"));
      e.target.classList.add("active");
      renderProducts(e.target.dataset.category);
    });
  });
}

// Cargar Detalle de Producto
function loadProductDetail() {
  const params = new URLSearchParams(window.location.search);
  const id = params.get("id");
  const product = products.find((p) => p.id === id);

  if (!product) {
    document.getElementById("product-detail-container").innerHTML =
      "<p>Producto no encontrado</p>";
    return;
  }

  // Set Main Info
  document.getElementById("main-image").src = product.image;
  document.getElementById("product-name").textContent = product.name;
  document.getElementById("product-price").textContent = formatter.format(
    product.price
  );
  document.getElementById("product-desc").innerHTML = product.description;


  // WhatsApp Link
  const message = `Hola, me interesa el ${product.name} que vi en Promesa de Angel.`;
  const waLink = `https://wa.me/5654229610?text=${encodeURIComponent(
    message
  )}`;
  document.getElementById("whatsapp-link").href = waLink;

  // Gallery Thumbs
  const thumbsContainer = document.getElementById("gallery-thumbs");
  thumbsContainer.innerHTML = "";

  product.images.forEach((imgSrc, index) => {
    const img = document.createElement("img");
    img.src = imgSrc;
    img.className = `thumb ${index === 0 ? "active" : ""}`;
    img.onclick = () => {
      document.getElementById("main-image").src = imgSrc;
      document.querySelectorAll(".thumb").forEach((t) =>
        t.classList.remove("active")
      );
      img.classList.add("active");
    };
    thumbsContainer.appendChild(img);
  });

  const mlBtn = document.getElementById("mercado-libre-btn");
  if (mlBtn) {
    if (product.inStock) {
      mlBtn.href = product.mercadoLibreLink || "#";
      mlBtn.style.display = "flex";
    } else {
      mlBtn.style.display = "none";
    }
  }
}